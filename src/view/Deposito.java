    private void btnDepositarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDepositarActionPerformed
        String ValorDeposito = DepositoText.getText();
        try {
            double valor = Double.parseDouble(ValorDeposito);
            if (valor > 0) {
                Controle.depositar(valor);
                atualizarTextAreaComSaldos(ControleLogin.getCpfUsuarioLogado());
            } else {
                JOptionPane.showMessageDialog(null, "Por favor, insira um valor positivo.");
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Por favor, insira um valor v√°lido.");
        }       
    }//GEN-LAST:event_btnDepositarActionPerformed
    private void atualizarTextAreaComSaldos(String cpf) {
    Map<String, Double> saldos = Controle.obterSaldos(cpf);
    String texto = String.format(
        "Saldo em Reais: R$ %.2f\n" +
        "Saldo em Bitcoin: %.5f BTC\n" +
        "Saldo em Ethereum: %.5f ETH\n" +
        "Saldo em Ripple: %.5f XRP",
        saldos.get("Reais"),
        saldos.get("Bitcoin"),
        saldos.get("Ethereum"),
        saldos.get("Ripple")
    );
    SaldoArea.setText(texto);
}
    
